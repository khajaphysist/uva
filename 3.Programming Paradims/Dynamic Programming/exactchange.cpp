template<class T>
inline void pv(T const & v, char c){
  for (auto it = v.begin(); it != v.end(); ++it) {
    cout << *it;
    auto ite = it;
    if (++ite == v.end())continue;
    else if(c!='x'){cout << c;}
  }cout << endl;
}

template<class T>
inline void pa(T const v[],int n, char c){
  for (int i = 0; i < n; ++i) {
    cout << v[i];
    if (i==n-1)continue;
    else if(c!='x'){cout << c;}
  }
  cout << endl;
}


template<class T>
inline void pvv(T const & vv, char c){
  for (auto it = vv.begin(); it != vv.end(); ++it) {
    pv(*it,c);
  }
}


inline void iv(vi &v,int n){
  for (int i = 0; i < n; ++i) {
    int temp;
    scanf("%d",&temp);
    v.push_back(temp);
  }
}

inline void is(si &v,int n){
  for (int i = 0; i < n; ++i) {
    int temp;
    scanf("%d",&temp);
    v.insert(temp);
  }
}

template<class T>
inline void ia(T a[],int n){
  for (int i = 0; i < n; ++i) scanf("%d",&a[i]);
}

//global variables
int n,purse[110];
int table[10050][2];

ii change(int p, int m){
  if(p<=0) return make_pair(p,m);
  if(table[p][0]!=-1 && table[p][1] <= m)return make_pair(table[p][0],table[p][1]);
  ii ret(-10000,100);
  for (int i = 0; i < n; i++) {
    int temp = purse[i];
    if(temp == -1)continue;
    purse[i] = -1;
    ii ans = change(p-temp,m+1);
    if(ret.first == ans.first) ret = ret.second < ans.second ? ret : ans;
    else if(ans.first == 0) ret = ans;
    else ret = ret.first > ans.first ? ret : ans;
    purse[i] = temp;
  }
  table[p][0] = ret.first;
  table[p][1] = ret.second;
  return ret;
}

int main(){
  int t;
  cin >> t;
  memset(table,-1,sizeof(table));
  for (int ji = 0; ji < t; ++ji) {
    int p; cin >> p; cin >> n;
    ia(purse,n);
    ii ans = change(p,0);
    cout << p-ans.first << " " << ans.second << endl;
    memset(table,-1,sizeof(table));
  }
}
